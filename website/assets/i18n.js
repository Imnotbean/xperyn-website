// Language switcher logic
let currentLang = 'en';

function setLanguage(lang) {
  currentLang = lang;
  // Method 1: data-i18n attributes
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const t = translations[key];
    if (t && t[lang]) {
      if (el.tagName === 'INPUT' && el.hasAttribute('placeholder')) {
        el.placeholder = t[lang];
      } else {
        el.textContent = t[lang];
      }
    }
  });

  // Method 2: Match by English text for elements without data-i18n
  const textMap = {};
  Object.values(translations).forEach(t => {
    if (t.en && t.zh) textMap[t.en.trim()] = t[lang];
  });

  const walker = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT, null);
  while (walker.nextNode()) {
    const node = walker.currentNode;
    const parent = node.parentElement;
    if (!parent || parent.closest('[data-i18n]') || parent.tagName === 'SCRIPT' || parent.tagName === 'STYLE') continue;
    const trimmed = node.textContent.trim();
    if (trimmed && textMap[trimmed]) {
      node.textContent = node.textContent.replace(trimmed, textMap[trimmed]);
    }
  }

  // Update toggle button states
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('lang-active', btn.dataset.lang === lang);
  });
  localStorage.setItem('xperyn-lang', lang);
  document.documentElement.lang = lang === 'zh' ? 'zh-CN' : 'en';
}

function initLanguage() {
  const saved = localStorage.getItem('xperyn-lang');
  const lang = saved || (navigator.language.startsWith('zh') ? 'zh' : 'en');
  if (lang === 'zh') setLanguage('zh');
}

// Auto-init when DOM is ready
if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', initLanguage);
} else {
  initLanguage();
}
